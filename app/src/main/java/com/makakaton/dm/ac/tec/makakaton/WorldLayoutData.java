    public static float[] rotARTE(float[] array, String pos) {
        float[] newArray = new float[array.length];
        System.arraycopy(array, 0, newArray, 0, array.length);
        switch (pos) {
            case "N":

                break;
            case "O":
                for (int i = 0; i < array.length; i = i + 3) {
                    newArray[i] = 0;
                    newArray[i + 2] = -1 * array[i];
                }
                break;
            case "S":
                for (int i = 0; i < array.length; i = i + 3) {
                    newArray[i] = newArray[i] * -1;
                }
                break;
            case "E":
                for (int i = 0; i < array.length; i = i + 3) {
                    newArray[i] = 0;
                    newArray[i + 2] = array[i];
                }
                break;
            case "UP":
                for (int i = 0; i < array.length; i = i + 3) {
                    newArray[i + 1] = 0;
                    newArray[i + 2] = array[i + 1];
                }
                break;
        }
        return newArray;
    }

    public static double makeItWrong(double result) {
        double wrongR = 0;
        Random random = new Random();
        double prob = Math.random();
        if (prob <= 0.5) {
            wrongR = result + random.nextInt(5) + 1;
        } else {
            wrongR = result - random.nextInt(5) + 1;
        }
        return wrongR;
    }
